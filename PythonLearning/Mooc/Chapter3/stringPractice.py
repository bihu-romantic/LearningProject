#!/usr/bin/python3
str = "Runoob";

print(str);        #输出字符串
print(str[0:-1]);  #output first to last before 2
print(str[0]);     #输出字符串第一个字符
print(str[2:5]);   #输出从第三个开始到第五个的字符
print(str[2:]);    #输出从第三个开始的后的所有字符
print(str * 2);    #输出字符串两次
print(str + " 你好");   #连接字符串
print("--------------------------");
print("hello\nrunoob");
print(r"hello\nrunoob");

input("\n\n 按下enter键后退出");
